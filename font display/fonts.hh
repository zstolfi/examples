struct CharData {
	unsigned w, h;
	std::vector<bool> pixels;

	CharData(unsigned w, unsigned h, const char* s)
	: w{w}, h{h}
	{
		pixels.resize(w*h);
		for (std::size_t i=0; i < w*h; i++) {
			pixels[i] = s[i] != ' ';
		}
	}
};

